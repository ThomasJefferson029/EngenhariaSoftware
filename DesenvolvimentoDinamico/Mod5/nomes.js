// Definindo uma string de exemplo
let nome = "Thomas Jefferson"
console.log("Nome inicial: " + nome);

// Usando o metodo toLowerCase()
let nomeMinusculo = nome.toLowerCase();
console.log("Nome em minusculos: " + nomeMinusculo);

// Usando o metodo toUpperCase()
let nomeMaiusculo = nome.toUpperCase();
console.log("Nome Maiusculo: " + nomeMaiusculo);

